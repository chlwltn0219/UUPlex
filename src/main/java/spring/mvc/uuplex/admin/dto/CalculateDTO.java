package spring.mvc.uuplex.admin.dto;

public class CalculateDTO {
	
	private String monthCal;
	private int m_totPrice;
	private int h_totPrice;
	private int f_totPrice;
	
	
	public String getMonthCal() {
		return monthCal;
	}
	public void setMonthCal(String monthCal) {
		this.monthCal = monthCal;
	}
	public int getM_totPrice() {
		return m_totPrice;
	}
	public void setM_totPrice(int m_totPrice) {
		this.m_totPrice = m_totPrice;
	}
	public int getH_totPrice() {
		return h_totPrice;
	}
	public void setH_totPrice(int h_totPrice) {
		this.h_totPrice = h_totPrice;
	}
	public int getF_totPrice() {
		return f_totPrice;
	}
	public void setF_totPrice(int f_totPrice) {
		this.f_totPrice = f_totPrice;
	}
	
	
}
